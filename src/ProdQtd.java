
public class ProdQtd {

	private int qtd;
	private int cod;
	
	public ProdQtd(int cod, int qtd) {
		this.cod = cod;
		this.qtd = qtd;
	}
	
	public int getQtd() {
		return qtd;
	}
	public void setQtd(int qtd) {
		this.qtd = qtd;
	}
	public int getCod() {
		return cod;
	}
	public void setCod(int cod) {
		this.cod = cod;
	}
	
}
